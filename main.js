

function makeUrduPresentTense(urduGerund,englishGerund) {
  console.log("mein "+ urduGerund + " raheenhun");
  console.log("I am "+ englishGerund + "ing")
}

function makeUrduCommand(urduGerund,englishGerund) {
  console.log(urduGerund + "o!");
  console.log(englishGerund+"!")
}

function makeUrduVerbs(urduGerund,englishGerund) {
  makeUrduPresentTense(urduGerund,englishGerund)
  makeUrduCommand(urduGerund,englishGerund)
  
}
makeUrduVerbs("seek","learn")

